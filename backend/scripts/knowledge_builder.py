def generate_knowledge_files():
    """生成知识库文件"""
    total_entries = 0

    for category, entries in FINANCIAL_KNOWLEDGE.items():
        filename = f"{category.replace('/', '_')}.md"
        filepath = KNOWLEDGE_DIR / filename

        with open(filepath, "w", encoding="utf-8") as f:
            f.write(f"# {category}\n\n")
            for entry in entries:
                f.write(f"## {entry['title']}\n\n")
                f.write(f"{entry['content']}\n\n")
                total_entries += 1

        print(f"[OK] 已生成 {filename}，包含 {len(entries)} 条知识")

    print(f"\n[INFO] 总计生成 {total_entries} 条知识库条目")
    return total_entries

if __name__ == "__main__":
    generate_knowledge_files()
